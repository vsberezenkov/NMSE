    /// <summary>
    /// Returns the object list display name for an object ID.
    /// This is the hardcoded name used for alphabetical sorting.
    /// Returns empty string if not in the map.
    /// </summary>
    internal static string GetPartDisplayName(string objectId)
    {
        if (string.IsNullOrEmpty(objectId))
            return "";

        string id = objectId;
        if (id.Length > 0 && id[0] != '^')
            id = "^" + id;

        if (DisplayNameMap.TryGetValue(id, out var name))
            return name;

        return "";
    }


    // ----------------------------------------------------------------
    //  Public API
    // ----------------------------------------------------------------

    /// <summary>
    /// Stores a reference to the game item database for display name lookups.
    /// Call this once after <see cref="GameItemDatabase.LoadItemsFromJsonDirectory"/>.
    /// </summary>
    internal static void LoadFromDatabase(GameItemDatabase database)
    {
        _database = database;
        _loaded = true;
    }

    /// <summary>
    /// Returns the category for a corvette building object.
    /// Uses a hardcoded functional part lists.
    /// Returns <see cref="OtherPriority"/> for non-functional parts.
    /// </summary>
    internal static int GetOptimizerPriority(string objectId)
    {
        if (string.IsNullOrEmpty(objectId))
            return OtherPriority;

        // Ensure ^ prefix for map lookup
        string id = objectId;
        if (id.Length > 0 && id[0] != '^')
            id = "^" + id;

        if (PriorityMap.TryGetValue(id, out var entry))
            return entry.category;

        return OtherPriority;
    }

    /// <summary>
    /// Returns the fixed sub-order position for a functional category part
    /// (categories 1 -> 4). Parts not in the map get int.MaxValue.
    /// </summary>
    internal static int GetSubOrder(string objectId)
    {
        string id = objectId;
        if (id.Length > 0 && id[0] != '^')
            id = "^" + id;

        if (PriorityMap.TryGetValue(id, out var entry))
            return entry.subOrder;

        return int.MaxValue;
    }

    /// <summary>
    /// Returns the display name for a building object, used for alphabetical
    /// sorting within the "Other" category.
    /// Looks up the item name in the game database (all item types, not just Corvette).
    /// Returns empty string if not found.
    /// </summary>
    internal static string GetDisplayName(string objectId)
    {
        if (string.IsNullOrEmpty(objectId))
            return "";

        // Primary: use object list display names for exact match
        string dispName = GetPartDisplayName(objectId);
        if (!string.IsNullOrEmpty(dispName))
            return dispName;

        // Fallback: use game item database
        if (_database != null)
        {
            string id = objectId;
            if (id.Length > 0 && id[0] == '^')
                id = id[1..];

            if (_database.Items.TryGetValue(id, out var item))
                return item.Name ?? "";
        }

        return "";
    }

    /// <summary>
    /// Whether the database lookup has been loaded.
    /// Used by tests to verify initialisation.
    /// </summary>
    internal static bool IsLoaded => _loaded;

    /// <summary>Clears the loaded data (for test isolation).</summary>
    internal static void Clear()
    {
        _database = null;
        _loaded = false;
    }

