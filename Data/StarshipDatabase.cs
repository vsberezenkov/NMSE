namespace NMSE.Data;

/// <summary>
/// Holds corvette part category and sort data for the corvette optimiser.
///
/// Optimizes in order of:
/// 1: Reactors
/// 2: Thrusters
/// 3: Wings/Boosters
/// 4: Landing Gears
/// 5: Landing Bays
/// 6: Cockpit
/// 7. Other (int.MaxValue)
///
/// Within categories 1 -> 4, parts are ordered by a fixed sub-order.
/// Categories 5 -> 6 preserve their original save order.
/// The "Other" group sorts alphabetically by display name (OrdinalIgnoreCase),
/// falling back to ObjectID when no display name is available.
/// </summary>
internal static class StarshipDatabase
{
    /// <summary>Reference to the game item database, used for display name lookups.</summary>
